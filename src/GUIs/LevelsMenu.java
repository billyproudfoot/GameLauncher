    public void updateTable() {
        String[] columns = {"Position", "Username", "Time Taken"};//Sets column names for table
        String[][] rows = new String[10][columns.length];//2D object array used for tableModel rows
        String time;
        for (int i = 0; i < 10; i++) { //Loops through all the rows in the database
            rows[i][0] = Integer.toString(i + 1);
            rows[i][1] = Methods.leaderboardQuery(jComboBox1.getSelectedIndex() + 1, "Username", i + 1); //Calls method to run SQL statement to get username
            rows[i][2] = Methods.leaderboardQuery(jComboBox1.getSelectedIndex() + 1, "Time_Taken", i + 1); //Calls method to run SQL statement to get timeetaken     
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                rows,
                columns
        ));
        jTable1.setDefaultEditor(Object.class, null);
        jLabel5.setText("Time: " + Methods.leaderboardQuery(jComboBox1.getSelectedIndex() + 1, "Time_Taken", username));
        jLabel6.setText("Position: " + Methods.leaderboardQuery(jComboBox1.getSelectedIndex() + 1, "Position", username));
    }

